// Function to get volume from sounding with linear interpolation
export function getSoundingVolume(tankData, sounding) {
  // Round to nearest even number (2cm increments)
  const roundedSounding = Math.round(sounding / 2) * 2;
  
  // If exact match exists
  if (tankData[roundedSounding] !== undefined) {
    return tankData[roundedSounding];
  }
  
  // Linear interpolation between two points
  const lowerSounding = Math.floor(sounding / 2) * 2;
  const upperSounding = Math.ceil(sounding / 2) * 2;
  
  const lowerVolume = tankData[lowerSounding] || 0;
  const upperVolume = tankData[upperSounding] || tankData[Math.max(...Object.keys(tankData).map(Number))];
  
  if (lowerSounding === upperSounding) {
    return lowerVolume;
  }
  
  // Linear interpolation
  const ratio = (sounding - lowerSounding) / (upperSounding - lowerSounding);
  return lowerVolume + (upperVolume - lowerVolume) * ratio;
}

